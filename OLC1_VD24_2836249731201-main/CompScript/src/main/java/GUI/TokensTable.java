    private void printTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tokensTable.getModel();
        
        int number = 1;
        Object[] row;
        
        for (int i = 0; i < tokensList.size(); i++) {
            row = new Object[5];
            row[0] = number;
            row[1] = tokensList.get(i).getLexeme();
            row[2] = tokensList.get(i).getType();
            row[3] = tokensList.get(i).getLine();
            row[4] = tokensList.get(i).getColumn();
            
            tableModel.addRow(row);
            number++;
        }
        
        centerTable();
    }
    
    private void centerTable() {
        DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer();
        cellRenderer.setHorizontalAlignment(SwingConstants.CENTER);
        
        for (int i = 0; i < tokensTable.getColumnCount(); i++) {
            tokensTable.getColumnModel().getColumn(i).setCellRenderer(cellRenderer);
        }
    }

